func TestDifferentialDecode(t *testing.T) {
	diffDecode := MakeDifferentialDecoder()
	data := diffDecode.Work(encodedDiffTestData)

	idx := tools.CompareByteArray(decodedDiffTestData, data, len(decodedDiffTestData))
	if idx != -1 {
		t.Errorf("  | Differential Encoding Error at test data pos %d. Expected %d got %d ", idx, decodedDiffTestData[idx], data[idx])
	}
}
